/**
* Make some assertions on our math library
*/

math.add(10, 5)
	.app.assert(is.equal(15))
.math.subtract(10, 5)
	.app.assert(is.equal(5))
.math.multiply(10, 5)
	.app.assert(is.equal(50))
.math.divide(10, 5)
	.app.assert(is.equal(2))
.math.add(10, [5, 10])
	.app.assert(is.equal([15, 20]))
.math.subtract(10, [5, 10])
	.app.assert(is.equal([5, 0]))
.math.multiply(10, [5, 10])
	.app.assert(is.equal([50, 100]))
.math.divide(10, [5, 10])
	.app.assert(is.equal([2, 1]))
.math.add([5, 10], 10)
	.app.assert(is.equal([15, 20]))
.math.subtract([5, 10], 10)
	.app.assert(is.equal([-5, 0]))
.math.multiply([5, 10], 10)
	.app.assert(is.equal([50, 100]))
.math.divide([5, 10], 10)
	.app.assert(is.equal([1/2, 1]))
.math.add([10, 20], [5, 10])
	.app.assert(is.equal([15, 30]))
.math.subtract([10, 20], [5, 10])
	.app.assert(is.equal([5, 10]))
.math.multiply([10, 20], [5, 10])
	.app.assert(is.equal([50, 200]))
.math.divide([10, 20], [5, 10])
	.app.assert(is.equal([2, 2]))
